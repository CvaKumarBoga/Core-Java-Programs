package com.app;

/*
 * Encapsulation simply means binding object state(fields) and object behavior(methods) 
 * into single entity.If you are creating class, you are doing encapsulation
 */
public class EncapsulationDemo {
	
	private int a = 10;
	int b = 20;
	
	public int getA() {
		return a;
	}
	public void setA(int a) {
		this.a = a;
	}
	public int getB() {
		return b;
	}
	public void setB(int b) {
		this.b = b;
	}

}

 